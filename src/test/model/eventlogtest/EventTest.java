package model.eventlogtest;

import model.eventlog.Event;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;

import java.util.Calendar;
import java.util.Date;

import static org.junit.jupiter.api.Assertions.assertEquals;

/**
 * Unit tests for the Event class
 *
 * ACKNOWLEDGEMENT: THIS TEST CODE HAS BEEN ENTIRELY TAKEN FROM THE EventTest CLASS OF
 * THE AlarmSystem PROJECT, WHICH CAN BE FOUND BELOW:
 * https://github.students.cs.ubc.ca/CPSC210/AlarmSystem
 *
 * LAST REVISED: 03/31/2022
 */

public class EventTest {
    private Event e;
    private Date d;

    //NOTE: these tests might fail if time at which line (2) below is executed
    //is different from time that line (1) is executed.  Lines (1) and (2) must
    //run in same millisecond for this test to make sense and pass.

    @BeforeEach
    public void runBefore() {
        e = new Event("Sensor open at door");   // (1)
        d = Calendar.getInstance().getTime();   // (2)
    }

    @Test
    public void testEvent() {
        assertEquals("Sensor open at door", e.getDescription());
        assertEquals(d.toString(), e.getDate().toString());
    }

    @Test
    public void testToString() {
        assertEquals(d.toString() + "\n" + "Sensor open at door", e.toString());
    }
}
